#include<bits/stdc++.h>
#include<stdio.h>

using namespace std;

int main() {
  long long a, b;
  scanf("%lld %lld", & a, & b);
  printf("%lld + %lld = %lld\n", a, b, a + b);
  printf("%lld * %lld = %lld\n", a, b, a * b);
  printf("%lld - %lld = %lld\n", a, b, a - b);

}